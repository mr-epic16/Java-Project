package ForLoop;

public class ForLoopSum {
	
		public static void main(String[] args) {
			
			int input = 100;
			
			int sum =0;
			
			for(int i =1;i<input;i=i+2)
			 {
				
				
				sum = sum+i;
				
				
			 }
		
			System.out.println("sum of numbers till "+ input + " is " + sum);
		
		
		}
		}


